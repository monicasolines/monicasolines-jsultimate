let a = 10;

/* siempre que use 
valores de compracion me devolveran un boolean (true or false)
*/

//operadores relaciones
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

//operadores de igualdad
console.log(a == 10);
console.log(a != 10);
console.log(a === '10');


/*dos == solo compara el valor por lo tanto
 console.log(a == 10); es true
 pero console.log(a === 10); ya que tres === 
 compara el tipo de variable y su valor */


